inline void SM_INVENTORY_RESPONSE(PACKET* pck)
{
	int num = 1;
	pck->CreateBufForSend();

	pck->writeD(0);
	pck->writeB(3);
	pck->writeB(mychar->items_in_inventory);
	pck->writeW(0);

	for (int i = 0; i < mychar->items_in_inventory; i++)
	{
		pck->writeUD(mychar->id);
		pck->writeUD(inv[i].PlayerItemUID);
		pck->writeUD(inv[i].item_id);
		pck->writeUD(inv[i].count);
		pck->writeUD(inv[i].equip);
		pck->writeUW(inv[i].sid);
		pck->writeUW(inv[i].slot);
	}

	pck->PackSend(OPCODE_SM_INVENTORY_RESPONSE);
	return;
}

